# -*- coding: utf-8 -*-
n1 = int(input("Informe o primeiro número inteiro: "))
n2 = int(input("Informe o segundo número inteiro: "))
n3 = float(input("Informe o número real: "))
print("\n")

produto = (n1 * 2) * (n2 / 2)
soma = (n1 * 3) + n3
potencia = n3 ** 3 

print ("O produto do dobro do primeiro com metade do segundo: " + str(produto) + "\n")
print("A soma do triplo do primeiro com o terceiro: " + str(soma) + "\n")
print("O terceiro elevado ao cubo: " + str(potencia))